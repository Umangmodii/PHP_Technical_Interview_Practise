<?php

$number = 40;

switch($number)
{
    case 10:
        echo "Number is Equal 10";
        break;
    
    case 20:
        echo "Number is Equal 20";
        break;

    case 30:
        echo "Number is Equal 30";
        break;

    case 40:
        echo "Number is Equal 40";
        break;

    default:
        echo "Number is Not Equals";
}